ORG 100H

START: 

MOV BL,CHOICE
CMP BL,01H
JE INTER40

MOV BL,CHOICE
CMP BL,02H
JE INTER41

MOV BL,CHOICE
CMP BL,03H
JE INTER42

MOV BL,CHOICE
CMP BL,04H
JE INTER43

INTER40:
MOV AX, 0000H       
MOV DS, AX
MOV AL, 40H    
MOV BL, 4H       
MUL BL          
MOV BX,AX
MOV SI, OFFSET[FUNC1]
MOV DS:[BX],SI
ADD BX,2                                 
MOV AX,CS     
MOV DS:[BX],AX
         
INT 40H   
JMP FINISH  

FUNC1:
 
PUSHA 
PUSH CS
POP DS

MOV AL,A
ADD AL,B
MOV ANS,AL
POPA
IRET


INTER41:
MOV AX, 0000H       
MOV DS, AX
MOV AL, 41H    
MOV BL, 4H       
MUL BL          
MOV BX,AX
MOV SI, OFFSET[FUNC2]
MOV DS:[BX],SI
ADD BX,2                                 
MOV AX,CS     
MOV DS:[BX],AX

INT 41H             
JMP FINISH  

FUNC2:
 
PUSHA 
PUSH CS
POP DS

MOV AL,A
MOV BL,B
CMP AL,BL
JA GO
SUB BL,AL
MOV ANS,BL
JMP SES
GO:
SUB AL,BL
MOV ANS,AL
SES:
POPA
IRET 


INTER42:

MOV AX, 0000H       
MOV DS, AX
MOV AL, 42H    
MOV BL, 4H       
MUL BL          
MOV BX,AX
MOV SI, OFFSET[FUNC3]
MOV DS:[BX],SI
ADD BX,2                                 
MOV AX,CS     
MOV DS:[BX],AX
INT 42H             
JMP FINISH  

FUNC3:
 
PUSHA 
PUSH CS
POP DS

MOV AL,A
MOV BL,B
MUL BL
MOV ANS,AL
POPA
IRET  



INTER43:

MOV AX, 0000H       
MOV DS, AX
MOV AL, 43H    
MOV BL, 4H       
MUL BL          
MOV BX,AX
MOV SI, OFFSET[FUNC4]
MOV DS:[BX],SI
ADD BX,2                                 
MOV AX,CS     
MOV DS:[BX],AX
INT 43H           
JMP FINISH  

FUNC4:
 
PUSHA 
PUSH CS
POP DS
MOV AX,0000H
MOV AL,A
MOV BL,B
CMP AL,BL
JA GO1
MOV AX,000H
MOV AL,B
MOV BL,A
DIV BL
MOV ANS,AL
MOV REMINDER,AH
JMP SES1
GO1:
MOV AX,0000H
MOV AL,A
MOV BL,B
DIV BL
MOV ANS,AL
MOV REMINDER,AH
SES1:
POPA
IRET 





FINISH:


A DB 06H
B DB 04H
CHOICE DB 02H
ANS DB ?
REMINDER DB ?
